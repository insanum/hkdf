void dump_key_material(int km_len, uint8_t *km)
{
    for (int i = 0; i < km_len; i++)
        printf("%02x", km[i]);
}

int check_key_material(uint8_t *exp_km, int km_len, uint8_t *km)
{
    for (int i = 0; i < km_len; i++) {
        if (exp_km[i] != km[i])
            return 0;
    }

    return 1;
}

